    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(!(getEmployee1_id()>0)){
            JOptionPane.showMessageDialog(this, "Please select the first employee");
            return;
        }
        if(!(getEmployee2_id()>0)){
            JOptionPane.showMessageDialog(this, "Please select the Second employee");
            return;
        }
        boolean b=UpdateDefaultEmployees();
        if(b){
            mwlp.setDefaultEmployee();
            mainView.selectComponent(mwlp);
            mainView.removeTab(this);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


