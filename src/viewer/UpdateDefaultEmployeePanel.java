    public boolean UpdateDefaultEmployees(){
        Employee e=new Employee();
        String sql="UPDATE `default_employee` SET `emp_1_id`='"+String.valueOf(getEmployee1_id())+"',`emp_2_id`='"+String.valueOf(getEmployee2_id())+"';";
        boolean b=e.update(sql);
        return b;
    }
    
    public void setEmployees(){
        Employee e=new Employee();
        String sql="SELECT * FROM `default_employee`";
        ResultSet rs=e.read(sql);
        try {
            if(rs.last()){
                setEmployee1_id(rs.getInt("emp_1_id"));
                setEmployee2_id(rs.getInt("emp_2_id"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateDefaultEmployeePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
