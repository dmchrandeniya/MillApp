    public void loadTable(String search){
        btnEdit.setEnabled(false);
        btnDelete.setEnabled(false);
        DefaultTableModel dtm=(DefaultTableModel)tblcategory.getModel();
        dtm.setRowCount(0);
        String sql="SELECT * FROM `expences_category`";
        if(search.length()>0){
            sql+=" WHERE `category` LIKE '%"+search+"%';";
        }
        ExpencesCategory ec=new ExpencesCategory();
        ResultSet rs=ec.read(sql);
        try {
            while(rs.next()){
                ec=new ExpencesCategory();
                ec.setId(rs.getInt("id"));
                ec.setCategory(rs.getString("category"));
                ArrayList al=new ArrayList();
                al.add(ec);
                dtm.addRow(al.toArray());
            }
        } catch (SQLException ex) {
            Logger.getLogger(ExpensesCategoryPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void save(){
        if(mainView.isEmptyField(txtCategory, "Please enter the category")){
            return;
        }
        if(getCategoryId()>0){
            ExpencesCategory ec=new ExpencesCategory();
            ec.setId(getCategoryId());
            ec.setCategory(txtCategory.getText());
            ExpensesCategoryController ecc=new ExpensesCategoryController();
            boolean b=ecc.update(ec);
            if(b){
                txtSearch.setText("");
                loadTable("");
                txtCategory.setText("");
            }
        }else{
            ExpencesCategory ec=new ExpencesCategory();
            ec.setCategory(txtCategory.getText());
            ExpensesCategoryController ecc=new ExpensesCategoryController();
            boolean b=ecc.create(ec);
            if(b){
                txtSearch.setText("");
                loadTable("");
                txtCategory.setText("");
            }
        }
        manageExpencesPanel.loadCategory();
        setCategoryId(0);
    }

