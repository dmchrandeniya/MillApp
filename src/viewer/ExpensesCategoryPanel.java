    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        ExpencesCategory ec=new ExpencesCategory();
        if(ec.checkCategory(expencesCategory.getId())){
            JOptionPane.showMessageDialog(this, "You can't delete this category. because this category is used.");
        }else{
            int i=JOptionPane.showConfirmDialog(this, "do you want to delete this category?");
            if(i==0){
                ExpensesCategoryController ecc=new ExpensesCategoryController();
                ecc.delete(expencesCategory);
                loadTable("");
                manageExpencesPanel.loadCategory();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        ExpencesCategory ec=new ExpencesCategory();
        if(ec.checkCategory(expencesCategory.getId())){
            JOptionPane.showMessageDialog(this, "You can't edit this category. because this category is used.");
        }else{
            setCategoryId(expencesCategory.getId());
            txtCategory.setText(expencesCategory.getCategory());
        }
        
        
    }//GEN-LAST:event_btnEditActionPerformed

