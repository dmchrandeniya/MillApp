    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        boolean b=inventoryCategory.checkCategoryUsed(inventoryCategory.getId());
        if(b){
            JOptionPane.showMessageDialog(this, "You can't delete this category. because this category is used.");
        }else{
            int i=JOptionPane.showConfirmDialog(this, "do you want to delete this category");
            if(i==0){
                InventoryCategoryController icc=new InventoryCategoryController();
                icc.delete(inventoryCategory);
                loadTable("");
                manageInventoryPanel.loadCategory();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


