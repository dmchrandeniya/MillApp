    public void loadTable(String search) {
        btnEdit.setEnabled(false);
        btnDelete.setEnabled(false);
        DefaultTableModel dtm = (DefaultTableModel) tblItemCategory.getModel();
        dtm.setRowCount(0);
        String sql = "SELECT * FROM `inventory_category`";
        if (search.length() > 0) {
            sql += " WHERE `category` LIKE '%" + search + "%';";
        } else {
            sql += ";";
        }
        InventoryCategory ic = new InventoryCategory();
        ResultSet rs = ic.read(sql);
        try {
            while (rs.next()) {
                ic = new InventoryCategory();
                ic.setId(rs.getInt("id"));
                ic.setCategory(rs.getString("category"));
                ArrayList<Object> al = new ArrayList();
                al.add(ic);
                dtm.addRow(al.toArray());
            }
        } catch (SQLException ex) {
            Logger.getLogger(ItemCategoryPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void save(){
        if(mainView.isEmptyField(txtCategory, "please enter the category")){
            return;
        }
        if(getCategoryId()>0){
            System.out.println("edit");
            InventoryCategory ic=new InventoryCategory();
            ic.setId(getCategoryId());
            ic.setCategory(txtCategory.getText());
            InventoryCategoryController icc=new InventoryCategoryController();
            boolean b=icc.update(ic);
            if(b){
                loadTable("");
                txtSearch.setText("");
                txtCategory.setText("");
            }
        }else{
            System.out.println("add");
            InventoryCategory ic=new InventoryCategory();
            ic.setCategory(txtCategory.getText());
            InventoryCategoryController icc=new InventoryCategoryController();
            boolean b=icc.create(ic);
            if(b){
                loadTable("");
                txtSearch.setText("");
                txtCategory.setText("");
            }
        }
        manageInventoryPanel.loadCategory();
        setCategoryId(0);
    }
    
